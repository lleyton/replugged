import stylus, { version } from 'stylus';
import Compiler from './compiler';
import { readFileSync } from 'fs';
import { dirname } from 'path';
import type Renderer from 'stylus/lib/renderer';

/**
 * Stylus compiler
 * @extends {Compiler}
 */
class Stylus extends Compiler {
  stylus: Renderer;

  constructor (file: string) {
    super(file);
    this.stylus = stylus(readFileSync(file, 'utf8'))
      .include(dirname(file));
  }

  async listFiles () {
    return [
      this.file,
      ...(this.stylus.deps())
    ];
  }

  _compile (): Promise<string> {
    return new Promise((resolve, reject) => {
      this.stylus.render((err, css) => {
        if (err) {
          return reject(err);
        }
        resolve(css);
      });
    });
  }

  get _metadata () {
    return `sytlus ${version}`;
  }
}

export default Stylus;
