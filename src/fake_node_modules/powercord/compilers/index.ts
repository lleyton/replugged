import CSSCompiler from './css';
import JsxCompiler from './jsx';
import ScssCompiler from './scss';
import LessCompiler from './less';
import StylusCompiler from './stylus';

export const css = CSSCompiler;
export const jsx = JsxCompiler;
export const scss = ScssCompiler;
export const less = LessCompiler;
export const stylus = StylusCompiler;
export function resolveCompiler (file: string) {
  switch (file.split('.').pop()) {
    case 'jsx':
      return new JsxCompiler(file);
    case 'scss':
      return new ScssCompiler(file);
    case 'less':
      return new LessCompiler(file);
    case 'styl':
      return new StylusCompiler(file);
    case 'css':
      return new CSSCompiler(file);
  }
}
